    public static function forDropdown(): array
    {
        return array_map(fn($c) => [
            'flag' => $c[0],
            'name' => $c[1],
            'dial' => $c[2],
            'nat'  => $c[3],
        ], self::$list);
    }

    public static function nationalities(): array
    {
        $seen = [];
        $result = [];
        foreach (self::$list as $c) {
            if (!in_array($c[3], $seen)) {
                $seen[] = $c[3];
                $result[] = ['nat' => $c[3], 'country' => $c[1]];
            }
        }
        return $result;
    }
